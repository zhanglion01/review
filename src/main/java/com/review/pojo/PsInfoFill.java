package com.review.pojo;

public class PsInfoFill {

  private String uuid;
  private String reName;
  private String rePrj;
  private String reYear;
  private String reUnit;
  public PsInfoFill(){}

  public String getUuid() {
    return uuid;
  }

  public void setUuid(String uuid) {
    this.uuid = uuid;
  }


  public String getReName() {
    return reName;
  }

  public void setReName(String reName) {
    this.reName = reName;
  }


  public String getRePrj() {
    return rePrj;
  }

  public void setRePrj(String rePrj) {
    this.rePrj = rePrj;
  }


  public String getReYear() {
    return reYear;
  }

  public void setReYear(String reYear) {
    this.reYear = reYear;
  }


  public String getReUnit() {
    return reUnit;
  }

  public void setReUnit(String reUnit) {
    this.reUnit = reUnit;
  }

}
